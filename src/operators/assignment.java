package operators;

public class assignment {
    public static void main(String args[]){
        int x=22;
        int age;
        age=x;
        System.out.println("age : "+(age));
        age+=x;
        System.out.println(""+(age));
        age-=x;
        System.out.println(""+(age));
        age*=x;
        System.out.println(""+(age));
        age/=x;
        System.out.println(""+(age));
        age%=x;
        System.out.println(""+(age));
        age&=x;
        System.out.println(""+(age));
        age|=x;
        System.out.println(""+(age));
        age^=x;
        System.out.println(""+(age));
        age>>=x;
        System.out.println(""+(age));
        age<<=x;
        System.out.println(""+(age));

    }
}
